    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String ten = txtTen.getText();
        String cccd = txtCCCD.getText();
        String chieucao = txtChieuCao.getText();
        String cannang = txtCanNang.getText();
        String loaibenh = txtLoaiBenh.getText();
        if (ten.isEmpty() || cccd.isEmpty() || chieucao.isEmpty() || cannang.isEmpty() || loaibenh.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
        } else if (ten.matches("[a-z A-Z]+") == false || loaibenh.matches("[a-z A-Z]+") == false) {
            JOptionPane.showMessageDialog(this, "Tên/Loại bệnh Sai định dạng");
        } else if (cccd.matches("\\d+") == false) {
            JOptionPane.showMessageDialog(this, "CCCD Sai định dạng");
        } else if (cannang.matches("\\d+(\\.\\d+)?") == false || chieucao.matches("\\d+(\\.\\d+)?") == false) {
            JOptionPane.showMessageDialog(this, "Cân nặng/chiều cao Sai định dạng");
        } else {
            BenhNhan bn = layDuLieuForm();
            listBN.add(bn);
            hienLenBang();
        }
    }//GEN-LAST:event_btnThemActionPerformed

