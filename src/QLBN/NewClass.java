    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String ten = txtTen.getText();
        String cccd = txtCCCD.getText();
        int check = 0;
        ArrayList<BenhNhan> listTim = new ArrayList<>();
        if (ten.isEmpty() || cccd.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ tên và cccd");
        } else {
            for (BenhNhan x : listBN) {
                if (x.getTen().contains(ten) && x.getCCCD().contains(cccd)) {
                    listTim.add(x);
                    check++;
                }
            }
            if (check == 0) {
                JOptionPane.showMessageDialog(this, "Không có thông tin bệnh nhân");
            } else {
                listBN = listTim;
                hienLenBang();
            }
        }

    }//GEN-LAST:event_btnTimKiemActionPerformed

