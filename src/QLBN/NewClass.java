    public void hienLenBang() {
        model.setRowCount(0);
        for (BenhNhan x : listBN) {
            model.addRow(new Object[]{
                x.getTen(), x.getCCCD(), x.getGioiTinh() == 1 ? "Nam" : "Nữ", x.getChieuCao(), x.getCanNang(), x.getKhoaKhamBenh(), x.getLoaiBenh(), x.isNhapVien()
            });
        }
    }

    public void hienLenForm(BenhNhan bn) {
        txtTen.setText(bn.getTen());
        txtCCCD.setText(bn.getCCCD());
        if (bn.getGioiTinh() == 1) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        txtChieuCao.setText(String.valueOf(bn.getChieuCao()));
        txtCanNang.setText(String.valueOf(bn.getCanNang()));
        cboKhoa.setSelectedItem(bn.getKhoaKhamBenh());
        txtLoaiBenh.setText(bn.getLoaiBenh());
        chkCo.setSelected(bn.isNhapVien());
    }

    public BenhNhan layDuLieuForm() {
        return new BenhNhan(txtTen.getText(), txtCCCD.getText(), rdoNam.isSelected() ? 1 : 2, Double.parseDouble(txtChieuCao.getText()), Double.parseDouble(txtCanNang.getText()), cboKhoa.getSelectedItem().toString(), txtLoaiBenh.getText(), chkCo.isSelected());
    }

